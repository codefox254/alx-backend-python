#!/usr/bin/env python3
"""
The  module provides a function for finding the floor of a float.
"""
import math


def floor(n: float) -> int:
    """
    The function finds the floor of a float and returns the result.

    Parameters:
    n (float): The float to find the floor of.

    Returns:
    int: The floor of n.
    """
    return math.floor(n)
